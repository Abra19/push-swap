/*
** return 0 if c is not character and not digit and 1 othrwise
** IN: int c
** OUT: true (1) or false (0)
*/

int		ft_isalnum(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122) || (c >= 48 && c <= 57))
		return (1);
	return (0);
}
